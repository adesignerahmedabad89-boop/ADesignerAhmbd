// Generates lib/work-images.ts from everything in /public/Stationary Design.
// Run with:  node scripts/gen-work-images.mjs
import { readdirSync, statSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const ROOT = join(process.cwd(), "public", "Stationary Design");
const IMG = /\.(jpe?g|png|webp|gif|avif)$/i;

const folders = readdirSync(ROOT).filter((f) => {
  try { return statSync(join(ROOT, f)).isDirectory(); } catch { return false; }
});

const manifest = {};
for (const folder of folders.sort()) {
  const files = readdirSync(join(ROOT, folder))
    .filter((f) => IMG.test(f))
    .sort();
  manifest[folder] = files.map((f) => `/Stationary Design/${folder}/${f}`);
}

const body = Object.entries(manifest)
  .map(([folder, imgs]) => {
    const list = imgs.map((p) => `    ${JSON.stringify(p)},`).join("\n");
    return `  ${JSON.stringify(folder)}: [\n${list}\n  ],`;
  })
  .join("\n");

const out = `// AUTO-GENERATED by scripts/gen-work-images.mjs — do not edit by hand.
// Every image under /public/Stationary Design, grouped by its folder.
export const workImages: Record<string, string[]> = {
${body}
};
`;

writeFileSync(join(process.cwd(), "lib", "work-images.ts"), out);
const total = Object.values(manifest).reduce((n, a) => n + a.length, 0);
console.log(`Wrote lib/work-images.ts — ${folders.length} folders, ${total} images.`);
